#ifndef CAMERA2D_H
#define CAMERA2D_H

/**
 * @file
 * @brief Math library.
 */

#include <types/bundle.h>

static inline f32v2 camera_center_offset  (f32v2 viewport, f32v2 center);

////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

f32v2 camera_center_offset(f32v2 viewport, f32v2 center)
{
	return (f32v2){
		center.x - viewport.w / 2.f,
		center.y - viewport.h / 2.f,
	};
}

#endif // CAMERA2D_H
